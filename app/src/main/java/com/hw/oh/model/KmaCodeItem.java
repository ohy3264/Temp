public class KmaCodeItem {
  String area;
  String locality;
  String thoroughfare;
  String kmacode;
  String kmaX;
  String kmaY;
  float distance;

  public String getArea() {
    return area;
  }

  public void setArea(String area) {
    this.area = area;
  }

  public String getLocality() {
    return locality;
  }

  public void setLocality(String locality) {
    this.locality = locality;
  }

  public String getThoroughfare() {
    return thoroughfare;
  }

  public void setThoroughfare(String thoroughfare) {
    this.thoroughfare = thoroughfare;
  }

  public String getKmacode() {
    return kmacode;
  }

  public void setKmacode(String kmacode) {
    this.kmacode = kmacode;
  }

  public String getKmaX() {
    return kmaX;
  }

  public void setKmaX(String kmaX) {
    this.kmaX = kmaX;
  }

  public String getKmaY() {
    return kmaY;
  }

  public void setKmaY(String kmaY) {
    this.kmaY = kmaY;
  }

  public float getDistance() {
    return distance;
  }

  public void setDistance(float distance) {
    this.distance = distance;
  }
}
